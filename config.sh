#!/bin/bash
# env-ubuntu 全局配置文件
# 根据需要修改以下变量

#=============================================================================
# 用户配置（自动检测，通常无需修改）
#=============================================================================
USER_NAME="${USER:-$(whoami)}"
USER_HOME="${HOME:-/home/$USER_NAME}"

#=============================================================================
# 路径配置
#=============================================================================
LOCAL_BIN="$USER_HOME/.local/bin"
SHARE_DIR="$USER_HOME/share"
APPLICATIONS_DIR="$USER_HOME/.local/share/applications"
ICONS_DIR="$USER_HOME/.local/share/icons/hicolor"

#=============================================================================
# 开发工具路径
#=============================================================================
ESP_IDF_PATH="$USER_HOME/esp/esp-idf"
RTK_PROJECT_PATH="$USER_HOME/rtk-project/rtk-work-Rannk-gitea/spbe-rtk"
ARM_TOOLCHAIN_PATH="$LOCAL_BIN/gcc-arm-none-eabi-10.3-2021.10/bin"

#=============================================================================
# 功能开关（true/false）
#=============================================================================
INSTALL_GCC=true
INSTALL_ZSH=true
INSTALL_FCITX=true
INSTALL_LIBREOFFICE=true
INSTALL_UDEV_RULES=true
INSTALL_FONTS=true
INSTALL_DEV_TOOLS=true

#=============================================================================
# GCC 版本配置
#=============================================================================
# 默认 GCC 版本优先级（数字越大优先级越高）
GCC_11_PRIORITY=110
GCC_12_PRIORITY=120
GCC_13_PRIORITY=130

#=============================================================================
# USB 设备规则
#=============================================================================
# STM32 设备 VID:PID（支持多个，用空格分隔）
STM32_DEVICES=(
    "0483:df11"  # STM32 DFU
    "0483:5740"  # STM32 Virtual COM Port
    "2e3c:df11"  # AT32 DFU (兼容 STM32)
)

#=============================================================================
# 项目根目录（自动检测）
#=============================================================================
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
